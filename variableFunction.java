public class variableFunction{
    public static void main(String[] args) {
        int a = 10;
        int b = 30;
        int sum = a + b;
        //addition
        System.out.println(sum);
        int diff = a-b;
        //difference
        System.out.println(diff);
        int mul = a*b;
        //multiply
        System.out.println(mul);
    }
}
